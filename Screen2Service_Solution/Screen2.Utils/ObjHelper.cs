        public static string GetStringFromInts(int[] inlist, string delimiter)
        {
            string resultString = "";

            for (int i = 0; i < inlist.Length; i++)
            {
                resultString += inlist[i].ToString();
                if (i < inlist.Length - 1)
                {
                    resultString += delimiter;
                }
            }


            return resultString;
        }


